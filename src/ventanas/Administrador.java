    public Administrador() {
        initComponents();
        user = Login.user;
        sesion_usurio = 1;
        
        setSize(650, 430);
        setResizable(false);
        setTitle("Administrador - Sesion de " + user);
        setLocationRelativeTo(null);
        
        setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
        
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaper.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(), jLabel_Wallpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select nombre_usuario from usuarios where username = '" + user + "'");
            
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                nombre_usurio = rs.getString("nombre_usuario");
                jLabel_NombreUsuario.setText("Bienvenid@ " + nombre_usurio);
            }
        } catch (Exception e) {
            System.err.println("Error en conexion desde la interfaz Administrador");
        }
    }
    
